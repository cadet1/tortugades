		ps.println("    Class enclosingClass,");
		ps.println("    String accessMethodName,");
		ps.println("    String enclosingThisName)");
		ps.println("  {");
//		ps.println("    System.out.println(\"addTANM \" + triggerClass + \" \" + notifiers);");

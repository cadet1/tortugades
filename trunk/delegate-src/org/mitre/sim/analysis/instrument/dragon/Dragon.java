		ps.println("      methodLookupArgs[0] = enclosingClass;");
		ps.println("      Method accessMethod = enclosingClass.getDeclaredMethod(");
		ps.println("        accessMethodName, methodLookupArgs);");
		ps.println("      Field enclosingThisField = triggerClass.getDeclaredField(enclosingThisName);");
		ps.println("      enclosingThisField.setAccessible(true);");
		ps.println("      Object[] fieldAccessArgs = new Object[1];");
		ps.println("      fieldAccessArgs[0] = enclosingThisField.get(trigger);");
		ps.println("      Object coll = accessMethod.invoke(null, fieldAccessArgs);");

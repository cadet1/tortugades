  private Method getInitializeMethod(LogicalProcess lp) {
    Class<?> c = lp.getDelegate().getClass();
    while (c != EntityDelegate.class) {
      try {
        return (c.getDeclaredMethod("initialize"));
      }
      catch (Throwable t) {
        c = c.getSuperclass();
      }
    }
    return null;
  }

  private void validateLPforTimeManagement(LogicalProcess lp) {
    if (!flowControl.sameAsMe(lp)) {
      throw new IllegalStateException("Entity " + lp.getName() +
          " attempted to provide time management services to an entity other than itself.");
    }
    if (!lp.isRunning())
      throw new IllegalStateException("Entity " + lp.getName() +
          " is not running and cannot employ time management services.");
    if (!lp.hasAgenda())
      throw new IllegalStateException("Entity " + lp.getName() +
          " does not have an agenda method and thus cannot employ time services.");
  }

  private void invokeEntityComplete(LogicalProcess lp) {
    if (!lp.entityCompleteInvoked) {
      try {
        lp.entityComplete();
      }
      catch (Exception e) {
        try {
          lp.exceptionHandler(e);
        }
        catch (Exception e1) {}
      }
      lp.entityCompleteInvoked = true;
      entityEventSupport.fireEntityEvent(lp, EntityEvent.COMPLETED);
    }
  }


  public Set<LogicalProcess> getLogicalProcesses() {
    Set<Event> allEvents = eventManager.getEvents();
    HashSet<LogicalProcess> result = new HashSet<LogicalProcess>();
    for (Iterator<Event> i = allEvents.iterator(); i.hasNext(); ) {
      Event event = (Event)i.next();
      if (LogicalProcess.class.isAssignableFrom(event.getClass())) {
        result.add((LogicalProcess)event);
      }
    }
    if (curentlyActiveLP != null) {
      result.add(curentlyActiveLP);
    }
    return result;
  }

  public void pauseExecutive() {
  	pausingLock.lock();
  	try {
  		if (executiveIsPaused) return;
  		immediatePausePending = true;
  		while (!executiveIsPaused) {
  			try {
					executivePausedCondition.await();
				} catch (InterruptedException ignored) { }
  		}
  	}
  	finally {
  		pausingLock.unlock();
  	}
  }
  
  public void resumeExecutive() {
  	pausingLock.lock();
  	try {
  		if (!executiveIsPaused) return;
  		immediatePausePending = false;
  		executiveShouldPause = false;
  		executiveMayResumeCondition.signalAll();
  	}
  	finally {
  		pausingLock.unlock();
  	}
  }
  
  public void pauseAfter(double delay, SimEventListener aTimeAchievedListener) {
  	pausingLock.lock();
  	try {
  		this.pendingPauseTime = timeNow.getTime() + delay * 0.999999;
  		this.timeAchievedListener = aTimeAchievedListener;
  		pace.set(0);
  		immediatePausePending = false;
  		executiveShouldPause = false;
  		executiveMayResumeCondition.signalAll();
  	}
  	finally {
  		pausingLock.unlock();
  	}
  }

